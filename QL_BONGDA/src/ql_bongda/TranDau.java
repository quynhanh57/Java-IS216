    public void showTenGD() {
        ArrayList<String> tengd = new ArrayList();
        try {
            Connection con = null;
            con = sqlServerConnection.getMysSQLConnection();
            String sql = "SELECT TENGIAI FROM GIAIDAU";
            SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                tengd.add(rs.getString("TENGIAI"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        for (String str : tengd) {
            GiaiDau.addItem(str);
        }
    }
    
    /**
     * Ham lay ma GD
     * @return 
     */
    public String getMaGD() {
        try {
            Connection con = null;
            con = sqlServerConnection.getMysSQLConnection();
            String str = "SELECT MAG FROM GIAIDAU WHERE TENGIAI = ?";
            SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(str);
            String item = (String) GiaiDau.getSelectedItem();
            ps.setString(1, item);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                return rs.getString("MAG");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return null;
    }

    
    public void showTenCN() {
        ArrayList<String> doicn = new ArrayList();
        ArrayList<String> doikhach = new ArrayList();
        try {
            Connection con = null;
            con = sqlServerConnection.getMysSQLConnection();
            String sql = "SELECT TENDOI FROM CT_GD INNER JOIN DOIBONG ON CT_GD.MAD = DOIBONG.MAD WHERE MAG = ?";
            SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(sql);           
            ps.setString(1, getMaGD());
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                doicn.add(rs.getString("TENDOI"));
                doikhach.add(rs.getString("TENDOI"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        for (String str : doicn) {
            Doicn.addItem(str);
            Doikhach.addItem(str);
        }
        
    }
    
