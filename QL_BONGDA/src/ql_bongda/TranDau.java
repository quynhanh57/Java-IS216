    private void jBtThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtThemActionPerformed
        String ngayda = NgayDaBox.getText();
        String diadiem = DiaDiemBox.getText();
        String tiso = TiSoBox.getText();
        String cn = (String) Doicn.getSelectedItem();
        String khach = (String) Doikhach.getSelectedItem();
        
        if (cn.equals(khach)){
            JOptionPane.showMessageDialog(this, "Đội chủ nhà phải khác đội khách!");
        } else if (ngayda.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập ngày đá!");
        } else if (diadiem.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập địa điểm đá!");
        } else if (tiso.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tỉ số!");
        } else {
            try {
                String SQlQuery = "INSERT INTO TRANDAU (NGAYDA,DIADIEM,MAG,MADN,MADK,TISO) VALUES(?,?,?,?,?,?)";
                Connection con = null;
                con = sqlServerConnection.getMysSQLConnection();
                SQLServerPreparedStatement ps = (SQLServerPreparedStatement) con.prepareStatement(SQlQuery);
                ps.setString(1, ngayda);               
                ps.setString(2, diadiem);
                ps.setString(3, getMaGD());
                ps.setString(4, getMaDN());
                ps.setString(5, getMaDK());
                ps.setString(6, tiso);
                int rs = ps.executeUpdate();
                if (rs == 0) {
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi!");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm trận đấu thành công!");
                }
                               
            } catch (Exception e) {
                System.err.println(e);
            }      
            
        }
    }//GEN-LAST:event_jBtThemActionPerformed

